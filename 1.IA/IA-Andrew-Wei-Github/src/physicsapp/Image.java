
package physicsapp;

/**
 *
 * @author Andrew
 */
public class Image {
    
    private int x;
    private int y;
    
    public Image(int x, int y) {
	this.x = x;
	this.y = y;
    }
    
    public void setX(int x) {
	this.x = x;
    }
    
    public void setY(int y) {
	this.y = y;
    }
    
    public int getX() {
	return x;
    }
    
    public int getY() {
	return y;
    }
}
